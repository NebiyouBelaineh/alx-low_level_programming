#include "3-calc.h"

/**
 * op_add -	adds two integers
 * @a:		integer one
 * @b:		integer two
 *
 * Return:	Sum of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub -     substracts two integers
 * @a:          integer one
 * @b:          integer two
 *
 * Return:     (a - b)
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul -     multiplies two integers
 * @a:          integer one
 * @b:          integer two
 *
 * Return:     (a * b)
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div -     divides two integers
 * @a:          integer one
 * @b:          integer two
 *
 * Return:     (a / b)
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod -     finds mod of two integers
 * @a:          integer one
 * @b:          integer two
 *
 * Return:     (a % b)
 */

int op_mod(int a, int b)
{
	return (a % b);
}


